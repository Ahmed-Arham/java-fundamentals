/*
 * Topic: If Else Statement
 * Description: Check whether a number is even or odd
 */

public class IfElse {
    public static void main(String[] args) {

        int num = 15;

        if (num % 2 == 0) {
            System.out.println("Even Number");
        } else {
            System.out.println("Odd Number");
        }
    }
}
